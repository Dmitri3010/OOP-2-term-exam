namespace exam
{
    /// <summary>
    /// Логика взаимодействия для MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
            List<string> styles = new List<string> { "day", "night" };
            styleBox2.SelectionChanged += styleBox2_SelectionChanged;
            styleBox2.ItemsSource = styles;
        }


        private void styleBox2_SelectionChanged(object sender, SelectionChangedEventArgs e)
        {
            string style = styleBox2.SelectedItem as string;
            var uri = new Uri(style + ".xaml", UriKind.Relative);
            ResourceDictionary resourceDict = Application.LoadComponent(uri) as ResourceDictionary;
            Application.Current.Resources.Clear();
            Application.Current.Resources.MergedDictionaries.Add(resourceDict);
        }
    }
}
